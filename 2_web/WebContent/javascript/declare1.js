// 자바스크립트에선 모든 객체나 함수는 function 으로 선언
function funcRed(){
	document.body.style.backgroundColor='#ffaaaa';
	document.body.style.color='#ffffff';
}

function funcGreen(){
	document.body.style.backgroundColor='#aaffaa';
	document.body.style.color='#777777';
}

function funcBlue(){
	document.body.style.backgroundColor='#ccccff';
}





